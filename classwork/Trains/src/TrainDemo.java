class Train
{
	private int TrainNumber;
	private String TrainName;
	private int Source;
	private int Destination;
	private int Coaches;
	private int Stoppages;
	private boolean Pantry;
	private static int Seats;
	public int getTrainNumber() {
		return TrainNumber;
	}
	public void setTrainNumber(int trainNumber) {
		TrainNumber = trainNumber;
	}
	public String getTrainName() {
		return TrainName;
	}
	public void setTrainName(String trainName) {
		TrainName = trainName;
	}
	public int getSource() {
		return Source;
	}
	public void setSource(int source) {
		Source = source;
	}
	public int getDestination() {
		return Destination;
	}
	public void setDestination(int destination) {
		Destination = destination;
	}
	public int getCoaches() {
		return Coaches;
	}
	public void setCoaches(int coaches) {
		Coaches = coaches;
	}
	public int getStoppages() {
		return Stoppages;
	}
	public void setStoppages(int stoppages) {
		Stoppages = stoppages;
	}
	public boolean isPantry() {
		return Pantry;
	}
	public void setPantry(boolean pantry) {
		Pantry = pantry;
	}
	public static int getSeats() {
		return Seats;
	}
	public static void setSeats(int seats) {
		Seats = seats;
	}
	
	
}
